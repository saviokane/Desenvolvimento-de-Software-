package principal.pesssoa;

public class pessoas {

	private String cpf;
	private String nome;
	private double peso;
	
	public String getCpf() {
		return cpf;
	}
	
	public String getNome() {
		return nome;
	}
	
	public double getPeso() {
		return peso;
	}
	
	
	//Setters
	
	public void setcpf (String cpf) {
		this.cpf = cpf;
	}
	
	public void setnome (String nome) {
		this.nome = nome;
	}
	
	public void setpeso(double peso) {
		this.peso = peso;
	}
	
	
	
}
